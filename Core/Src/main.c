void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if(htim->Instance == TIM2) {
		updateled = 1;
	}

	if(htim->Instance == TIM3) {
		global_time++;

		if(global_time >= 1000) {
			global_time = 0;
		}

		if(global_time % 100 == 0) {
			lcd_update_requested = 1;
		}
	}
}

void ledFade(uint16_t system_ms) {
    uint32_t duty_cycle = 0;

    // Use the absolute system time instead of a local counter
    if(system_ms <= 600) {
        duty_cycle = 159 - ((159 * (uint16_t)system_ms) / 600);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, duty_cycle);
    } else {
        // Explicitly keep the LED off from 601ms to 999ms
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
    }


	}


void lcdUpdate() {
	HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
	HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);

	char timeINlcd[20];

	sprintf(timeINlcd, "%02d:%02d:%02d:%03d", sTime.Hours, sTime.Minutes, sTime.Seconds, global_time);

    lcd_set_color(YELLOW, BLACK);       // <-- set font BLACK, background CYAN
	lcd_show_string(8, 100, 32, timeINlcd);
}
